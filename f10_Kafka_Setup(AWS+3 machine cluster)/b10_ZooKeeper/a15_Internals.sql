
-- Internals

  # Has internal data structure like a Tree. ie
    + root
    + Branches
    + Leaf / Node (**zNode)

  # zNode
    + Can store data
    + Can be persistent or ephemeral
    + Each zNode has a path associated with it
    + Cannot be renamed


  # Watching
    + Zookeeper clients can watch zNode for changes
      (ie Zookeeper can notify client of any changes)

    


